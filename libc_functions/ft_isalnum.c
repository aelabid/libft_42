#include"libft.h"

int	ft_isalnum(int numTest)
{
	if (((numTest >= 'A' && numTest <= 'Z' )
			|| (numTest >= 'a' && numTest <= 'z'))
		|| (numTest >= '0' && numTest <= '9'))
		return (1);
	return (0);
}
