#include"libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*temp;

	if (*alst == NULL)
	{
		*alst = new;
		return ;
	}
	temp = *alst;
	temp = ft_lstlast(*alst);
	temp->next = new;
}
 